weather = 'snowy'

case weather

when 'sunny'
	puts "Be sure to remember your sunnies!"
when 'cloudy'
	puts "A light jacket might be a good idea today."
when 'rainy'
	puts "Remember your wellies today!"
when 'snowy'
	puts "Bundle up with a cozy coat, a scarf, hat, and mittens!"
else
  	puts "Hmm, I don't know what you should wear in that kind of weather!"
end
